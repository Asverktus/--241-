#include <iostream>
#include <iomanip>  
#include <cmath>

int main() {
    // Русский язык
    setlocale(LC_ALL, "rus");

    // Заданные параметры
    double lambda = 59.034;  // Теплопроводность, Вт/м·град
    double R1 = 0.252;       // Внутренний радиус, м
    double R2 = 0.293;       // Внешний радиус, м
    double t1 = 264.0;       // Температура на внутренней стороне, град
    double t2 = 43.0;        // Температура на внешней стороне, град
    double F = 0.53;         // Площадь поверхности плоской стенки, м^2
    double L = 1.0;          // Длина цилиндрической стенки, м

    // Расчет теплового потока через плоскую стенку
    double Q1 = (lambda * F) / (R2 - R1) * (t1 - t2);

    // Расчет теплового потока через цилиндрическую стенку
    double Q2 = (2 * acos(-1) * lambda * L) / log(R2 / R1) * (t1 - t2);

    // Расчет теплового потока через шаровую стенку
    double Q3 = (4 * acos(-1) * lambda) / (1 / R1 - 1 / R2) * (t1 - t2);

    // Вывод результатов с 6 знаками после запятой
    std::cout << std::fixed << std::setprecision(6);
    std::cout << "Тепловой поток через плоскую стенку: Q1 = " << Q1 << " Вт" << std::endl;
    std::cout << "Тепловой поток через цилиндрическую стенку: Q2 = " << Q2 << " Вт" << std::endl;
    std::cout << "Тепловой поток через шаровую стенку: Q3 = " << Q3 << " Вт" << std::endl;

    return 0;
}